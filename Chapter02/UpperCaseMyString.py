# This program will convert all lower case text
# to uppercase and vice versa

# intialize our var with small letters
myName = 'teodulfo espero'

# invoke a function to convert all lowercase letters to uppercase
convertedName = myName.upper()

# print out the new format
print(convertedName)

# turn it back to lowercase
convertedName = convertedName.lower()

# print out the new format
print(convertedName)

